""" OPERATORS
 Realtional operators  (<, <=,  >,  >=,  ==, !=)
"""
x = 15
y = 3
print("x = 15 y = 3")

#Greater than
result = x>y
print("x>y",result)

#Greater than equal to
result = x>=y
print("x>=y",result)

#Less than
result = x<y
print("x<y",result)

#Less than equal to
result = x<=y
print("x<=y",result)

#Equal to
result = x==y
print("x==y",result)

# Not equal to
result = x!=y
print("x!=y",result)



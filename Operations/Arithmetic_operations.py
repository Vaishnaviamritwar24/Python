""" OPERATORS
 Arthemetic Operators  (+,-,*,/, % //)
"""
x = 15
y = 3

print("Addition:")
Addition = x+y
print(Addition)

print("Subtraction:")
Subtraction = x-y
print(Subtraction)

print("Multiplication:")
Multiplication = x*y
print(Multiplication)

print("Power raised to:")
result = x**y
print(result)

print("Division:")
Division = x / y
print(Division)  # Output: 5.0 (floating-point division)

# For integer division (floor division), use double slashes:
result = x // y
print(result)  # Output: 5 (integer division)

print("Modulo:")
Remainder = x%y
print(Remainder)

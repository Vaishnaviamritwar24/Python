#Arithmetic

#Addition 
def add(a,b):
    print("sum is:",a+b)

#Subtraction
def sub(a,b):
    print("diff is:",a-b)

#Multiplication 
def mul(a,b):
    print("Product is:",a*b)

#Divison 
def div(a,b):
    print("div is:",a/b)

#Floor divison 
def floordiv(a,b):
    print("floordiv is:",a//b)

#Remainder (Modulo)
def rem(a,b):
    print("remainder is:",a%b)

    
